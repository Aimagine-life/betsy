export function buildPersonalityPrompt(values: Record<string, number>): string {
  const lines: string[] = [];

  for (const key of ALL_SLIDER_KEYS) {
    const value = values[key] ?? DEFAULT_PERSONALITY[key] ?? 2;
    const prompts = SLIDER_PROMPTS[key];
    if (prompts) {
      const index = Math.max(0, Math.min(4, Math.round(value)));
      lines.push(prompts[index]);
    }
  }

  return lines.join(". ") + ".";
}
